# 字串的用法
# \n是換行
print("Hello \nMr.White")
# \'是使符號性質變字串
print("Hello \'Mr.White")
# +使字串相連

# 函式 function
# lower寫法:字串.lower()，使字母變小寫
# upper寫法:字串.upper()，使字母變大寫
# isupper寫法:字串.isupper()，判斷字串內字母是否是大寫
# islower寫法:字串.islower()，判斷字串內字母是否是小寫
# 字串後加上[]
# []內加入一個數字，表示字串內字母的位置，從0開始。若有重複，取最前
# replace寫法:字串.replace(字串,字串)，後面替換前面