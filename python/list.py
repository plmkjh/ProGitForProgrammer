# 列表用法
# 變數 = [資料,資料]
scores = [90,70,60,50,80]
print(scores)
# 取列表之間之值:變數[數字:數字]
# 取列表目標之前之值:變數[:數字]
# 取列表目標之後之值:變數[數字:]
# 列表改值
scores[0] = 30
# 函式
# 列表.extend(列表)，列表合併
# 列表.append(值)，列表後新增一值
# 列表.insert(位置,值)，列表插入一值
# 列表.remove(值)，列表刪除此值
# 列表.clear()，清空此列表
# 列表.pop()，移除列表最後值
# 列表.sort()，列表內值依大小排列
# 列表.reverse()，列表內值順序反轉
# 列表.index(值)，從頭尋找列表內相同的值並回傳位置
# 列表.count(值)，從頭尋找列表內相同的值並回傳數量